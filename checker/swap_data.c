void		search_data(block *head)
{
	int		i;
	block	*temp1;
	block	*temp2;

	temp1 = head;
	temp2 = head;
	i = 1;
	while (i <= 1)
	{
		temp1 = temp1->next;
		i++;
	}
	i = 1;
	while (i <= 2)
	{
		temp2 = temp2->next;
		i++;
	}
}


